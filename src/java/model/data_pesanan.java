package model;

public class data_pesanan {
    
    private String id_pesanan, id_team, tgl_pesanan, jam, id_lapangan, durasi;
    
    public String getId_Pesanan() 
    {         
        return id_pesanan;
    }     

    public void setId_Pesanan(String id_pesanan)
    {         
        this.id_pesanan = id_pesanan;     
    } 
    
    public String getId_Team() 
    {         
        return id_team;
    }     

    public void setId_Team(String id_team)
    {         
        this.id_team = id_team;     
    }
    
    public String getTgl_Pesanan() 
    {         
        return tgl_pesanan;
    }     
 
    public void setTgl_Pesanan(String tgl_pesanan)
    {         
        this.tgl_pesanan = tgl_pesanan;     
    }
    
    public String getJam() 
    {         
        return jam;
    }     

    public void setJam(String jam)
    {         
        this.jam = jam;     
    }
    
    public String getId_Lapangan() 
    {         
        return id_lapangan;
    }     

    public void setId_Lapangan(String id_lapangan)
    {         
        this.id_lapangan = id_lapangan;     
    }
    
    public String getDurasi() 
    {         
        return durasi;
    }     

    public void setDurasi(String durasi)
    {         
        this.durasi = durasi;     
    }
    
}
